const area = 50 * 30;
const wasteMultiplier = 1 + 10 / 100;
const areaWithWaste = area * wasteMultiplier;
console.log('area:', area);
console.log('wasteMultiplier:', wasteMultiplier);
console.log('areaWithWaste:', areaWithWaste);
console.log('areaWithWaste / 10:', areaWithWaste / 10);
console.log('Math.ceil:', Math.ceil(areaWithWaste / 10));
console.log('Is it exact?', areaWithWaste / 10 === 165);
console.log('Binary repr:', (areaWithWaste / 10).toPrecision(20));
